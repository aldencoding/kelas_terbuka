#Operasi Aritmatika

a = 10
b = 5

#Operasi Tambah +
print("====Operasi Tambah====")

Hasil = a+b
print(a,"+",b,"= ", Hasil)

#Operasi Pengurangan -
print("====Operasi Pengurangan====")

Hasil = a-b
print(a,"-",b,"= ", Hasil)

#Operasi Perkalian *
print("====Operasi Perkalian====")

Hasil = a*b
print(a,"*",b,"= ", Hasil)

#Operasi Pembagian /
print("========Operasi Pembagian========")

Hasil = a/b
print(a,"/",b,"= ", Hasil)

#Operasi Eksponen Pangkat **
print("========Operasi Eksponen Pangkat========")

Hasil = a**b
print(a,"**",b,"= ", Hasil)

#Operasi Modulus %
print("========Operasi Modulus========")

Hasil = a%b
print(a,"%",b,"= ", Hasil)

#Operasi Floor Division //
print("========Operasi Floor Divison========")#Pembulatan kebawah

Hasil = a//b
print(a,"//",b,"= ", Hasil)

#Prioritas Operasi, Operational precedence
print("====Operational Precedence/Prioritas")

x = 3
y = 2
z = 4

Hasil = x * y + z
print(x,"*",y,"+",z,"= ",Hasil)

Hasil = x * (y + z)
print(x,"*","(",y,"+",z,")""= ",Hasil)


